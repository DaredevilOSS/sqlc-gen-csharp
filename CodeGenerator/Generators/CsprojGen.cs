using System;
using Google.Protobuf;
using File = Plugin.File;


namespace SqlcGenCsharp.Generators;

internal class CsprojGen(string projectName, DriverName driverName, DotnetFramework targetFramework)
{
    public File GenerateFile()
    {
        var csprojContents = GetFileContents();
        return new File
        {
            Name = $"{projectName}.csproj",
            Contents = ByteString.CopyFromUtf8(csprojContents)
        };
    }
    
    private string GetFileContents()
    {
        var nullableProperty = targetFramework == DotnetFramework.Dotnet80 ? "enable" : "disable";
        return $"""
                 <!--{Consts.AutoGeneratedComment}-->
                 <!--Run the following to add the project to the solution: 
                    dotnet sln add {projectName}/{projectName}.csproj
                   -->
                 <Project Sdk="Microsoft.NET.Sdk">
                 
                     <PropertyGroup>
                         <TargetFramework>{targetFramework.StandardName()}</TargetFramework>
                         <Nullable>{nullableProperty}</Nullable>
                         <OutputType>Library</OutputType>
                     </PropertyGroup>
                 
                     <ItemGroup>
                         <PackageReference Include="{driverName}" Version="{GetPackageVersion(driverName)}"/>
                     </ItemGroup>
                 
                 </Project>
                 """;
    }

    private static string GetPackageVersion(DriverName driverName)
    {
        return driverName switch
        {
            DriverName.Npgsql => "8.0.2",
            DriverName.MySqlConnector => "2.3.6",
            _ => throw new NotSupportedException($"Unsupported driver: {driverName}")
        };
    }
}